# -------------
# Escape Sequences Characters
# \b => back space
# \newline => Escape New Line + \
# \ => Escape Back Slash
# \ => Escape Single Quotes
# \ => Escape Double Quotes
# \n => Line Feed Character ( New Line )
# \r => Carriage Return
# \t => Horizontal Tab
# \xhh => Character Hex Value ( Google Letter Hex Value )
# ---------------

# Back Space
print('Hello\bWorld')

# Escape New line + \
print('Hello \
World \
and \
Python \
')

# Escape Back Slash
print('I Love Back Slash \\')

# Escape Single Quote
print('I Love Single Quotes \'Test\' ')

# Escape Double Quote
print("I Love Double Quotes \"Test\" ")

# Line Feed
print('Hello World\nSecond Line')

# Carriage Return
print("123456\rabcde")

# Horizontal Tab
print("Hello\tWorld")

# Character Hex Value
print("\x47\x65\x6f\x72\x67\x65")
