# --------------
# Variables Part One
# Syntax => [Variable Name] [Assignment Operator "="] [Value]
# --------------
# Variable Naming Rules =>
# [1] Can start With (A~Z) or (a~z) or (_)
# [2] Can't start with Numbers or Special Characters (@,-,$)
# [3] Can include NUM or (_)
# [4] Can't include Special Characters
# [5] Name is not like name [ Case Sensitive ]
# --------------
# Example:
_myVariable = "My Value"
variable = "My Value"
myVariable = "My Value"
My_Variable = "My Value"
print(_myVariable)
print(variable)  # Single Word
print(myVariable)  # camelCase
print(My_Variable)  # Snake_Case
